let number = parseInt(prompt("Digite um numero"))

switch (number) {
    case 1:
        alert("Domingo")
        break;

    case 2:
        alert("Segunda")
        break;

    case 3:
        alert("Terça")
        break;

    case 4:
        alert("quarta")
        break;
    case 5:
        alert("quinta")
        break;
    case 6:
        alert("Sexta")
        break;
    case 7:
        alert("Sabado")
        break;
    default:
        alert("Não a dia correspondente")


}